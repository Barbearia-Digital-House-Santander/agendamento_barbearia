$(document).ready(function(){
    
    (function($) {
        "use strict";

    
    jQuery.validator.addMethod('answercheck', function (value, element) {
        return this.optional(element) || /^\bcat\b$/.test(value)
    }, "type the correct answer -_-");

    // validate contactForm form
    $(function() {
        $('#contactForm').validate({
            rules: {
                name: {
                    required: true,
                    minlength: 2
                },
                subject: {
                    required: true,
                    minlength: 4
                },
                number: {
                    required: true,
                    minlength: 5
                },
                email: {
                    required: true,
                    email: true
                },
                message: {
                    required: true,
                    minlength: 20
                }
            },
            messages: {
                name: {
                    required: "Vamos lá, você tem um nome, não?",
                    minlength: "Seu nome precisa ter mais de dois caracteres"
                },
                subject: {
                    required: "Vamos lá, você tem um assunto para nos dar um feedback, não?",
                    minlength: "Seu assunto precisa ter mais de quatro caracteres"
                },
                number: {
                    required: "Vamos lá, você tem um número, não?",
                    minlength: "Seu número precisa conter mais de cinco caracteres alfanuméricos"
                },
                email: {
                    required: "sem email, sem mensagem"
                },
                message: {
                    required: "Hm... Você precisa digitar um feedback antes para poder nos enviar =)",
                    minlength: "isso é tudo? sério? =("
                }
            },
            submitHandler: function(form) {
                $(form).ajaxSubmit({
                    type:"POST",
                    data: $(form).serialize(),
                    url:"contact_process.php",
                    success: function() {
                        $('#contactForm :input').attr('disabled', 'disabled');
                        $('#contactForm').fadeTo( "slow", 1, function() {
                            $(this).find(':input').attr('disabled', 'disabled');
                            $(this).find('label').css('cursor','default');
                            $('#success').fadeIn()
                            $('.modal').modal('hide');
		                	$('#success').modal('show');
                        })
                    },
                    error: function() {
                        $('#contactForm').fadeTo( "slow", 1, function() {
                            $('#error').fadeIn()
                            $('.modal').modal('hide');
		                	$('#error').modal('show');
                        })
                    }
                })
            }
        })
    })
        
 })(jQuery)
})